package tp7.ArrayySorting;

import java.util.ArrayList;
import java.util.List;
import java.util.Arrays.*;

public class ArraysSorting {
    public static void iterararray(Integer[] listas) {
        for (Object s : listas) {
            System.out.print(s + " ");
        }
    }

    public static void iterarINt(List<Integer> listas) {
        for (int s : listas) {
            System.out.print(s + " ");
        }
    }

    public static void iterarObje(Object[] listas) {
        for (Object s : listas) {
            System.out.print(s + " ");

        }
    }

}
